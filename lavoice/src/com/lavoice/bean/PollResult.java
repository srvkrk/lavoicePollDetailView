package com.lavoice.bean;


public class PollResult{
	private String pollId;
	private String pollUserEmail;
	private String pollFinalResult;
	public String getPollId() {
		return pollId;
	}
	public void setPollId(String pollId) {
		this.pollId = pollId;
	}
	public String getPollUserEmail() {
		return pollUserEmail;
	}
	public void setPollUserEmail(String pollUserEmail) {
		this.pollUserEmail = pollUserEmail;
	}
	public String getPollFinalResult() {
		return pollFinalResult;
	}
	public void setPollFinalResult(String pollFinalResult) {
		this.pollFinalResult = pollFinalResult;
	}
	
	
}
